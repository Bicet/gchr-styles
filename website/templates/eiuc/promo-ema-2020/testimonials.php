function copyToClipboard(element) {
  var $temp = $("<textarea>");
  $("body").append($temp);
  var html = $(element).html();
  html = html.replace(/<br>/g, "\n"); // or \r\n
  html = html.replace(/&amp;/, "&");

  $temp.val(html).select();
  document.execCommand("copy");
  $temp.remove();
}

